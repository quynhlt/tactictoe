package com.example.tactictoe.model;

public class History {

	private String firstSign;
	private String winSign;
	private String sequenceStep;

	public String getFirstSign() {
		return firstSign;
	}

	public void setFirstSign(String firstSign) {
		this.firstSign = firstSign;
	}

	public String getWinSign() {
		return winSign;
	}

	public void setWinSign(String winSign) {
		this.winSign = winSign;
	}

	public String getSequenceStep() {
		return sequenceStep;
	}

	public void setSequenceStep(String sequenceStep) {
		this.sequenceStep = sequenceStep;
	}

}
