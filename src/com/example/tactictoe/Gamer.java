package com.example.tactictoe;

public class Gamer {
	private String[] values;
	private int[] moves;

	/**
	 * @return the moves
	 */
	public int[] getMoves() {
		return moves;
	}

	/**
	 * @param moves
	 *            the moves to set
	 */
	public void setMoves(int[] moves) {
		this.moves = moves;
	}

	/**
	 * @return the values
	 */
	public String[] getValues() {
		return values;
	}

	/**
	 * @param values
	 *            the values to set
	 */
	public void setValues(String[] values) {
		this.values = values;
	}

}
